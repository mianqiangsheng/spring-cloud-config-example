package org.example.dto;

public class ReduceBalanceRequest {

    private Long userId;

    private Integer price;

    public ReduceBalanceRequest() {
    }

    public ReduceBalanceRequest(Long userId, Integer price) {
        this.userId = userId;
        this.price = price;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }
}